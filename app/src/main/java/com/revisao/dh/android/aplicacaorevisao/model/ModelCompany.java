package com.revisao.dh.android.aplicacaorevisao.model;

public class ModelCompany {

    String title;
    String image;

    public ModelCompany(String title, String image) {
        this.title = title;
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
